#include <stdio.h>
#include "reader.h"

FILE *inputStream;
int lineNo, colNo;  // chi so dong, cot hien tai
int currentChar;    // ki tu hien tai dang doc duoc la ki tu nao

int readChar(void) {  // doc mot ki tu tu kenh vao
  currentChar = getc(inputStream);
  colNo ++;
  if (currentChar == '\n') {
    lineNo ++;
    colNo = 0;
  }
  return currentChar;
}

int openInputStream(char *fileName) { // mo kenh vao
  inputStream = fopen(fileName, "rt");
  if (inputStream == NULL)
    return IO_ERROR;
  lineNo = 1;
  colNo = 0;
  readChar();
  return IO_SUCCESS;
}

void closeInputStream() {     // dong kenh vao
  fclose(inputStream);
}

